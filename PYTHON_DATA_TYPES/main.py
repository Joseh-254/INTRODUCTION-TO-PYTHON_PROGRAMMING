# python have the folllowing datatypes
# Numeric data types: int, float, complex.
# String data types: str.
# Sequence types: list, tuple, range.
# Binary types: bytes, bytearray, memoryview.
# Mapping data type: dict.
# Boolean type: bool.
# Set data types: set, frozenset.

x = 6  #this is a numeric datatype

x = "this is a string"  #this is a string datatype

x = range(6)	#this is a range	

x = {"name" : "John", "age" : 36}	#this is a dictionary	datatype

x = {"apple", "banana", "cherry"}	#this is a set	datatype

x = frozenset({"apple", "banana", "cherry"})	#this is a frozenset	datatype

x = True	#this is a bool	datatype

x = b"Hello"	#this is a bytes	datatype

x = bytearray(5)	#this is a bytearraydatatype	

x = memoryview(bytes(5))	#this is a memoryview	datatype

x = None  #this is a  none datatype

x = 20.5	# this is a float datatype

x = 1j	#this is a complex datatype	

x = ["apple", "banana", "cherry"]	#this is a list datatype
	
x = ("apple", "banana", "cherry")	#this is a tuple datatype


